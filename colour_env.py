#这些是定义颜色的函数
def ingreen(s):
    return"%s[30;32;2m%s%s[0m"%(chr(27), s, chr(27))
def indgreen(s):
    return"%s[30;35;2m%s%s[0m"%(chr(27), s, chr(27))
def inyellow(s):
    return"%s[30;33;2m%s%s[0m"%(chr(27), s, chr(27))
def inred(s):
    return"%s[30;31;2m%s%s[0m"%(chr(27), s, chr(27))
	
	
	
